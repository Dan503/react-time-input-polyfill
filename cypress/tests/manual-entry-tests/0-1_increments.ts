import { a11yHasExpectedHtml, a11yInitialHtml, loadTestPage } from "../../support"

export const increments_0_1 = () => {
	describe('increments 0-1', () => {
		it('0 0 0 0 A', () => {
			loadTestPage({ segment: 'hrs12' })
				.type('0')
				.should('have.value', '12:30 PM')
				.then(a11yHasExpectedHtml('<p>12.</p>'))
				.type('0')
				.should('have.value', '12:30 PM')
				.then(a11yHasExpectedHtml(a11yInitialHtml().minutes))
				.type('0')
				.should('have.value', '12:00 PM')
				.then(a11yHasExpectedHtml('<p>0.</p>'))
				.type('0')
				.should('have.value', '12:00 PM')
				.then(a11yHasExpectedHtml(a11yInitialHtml().mode))
				.type('a')
				.should('have.value', '12:00 AM')
				.then(a11yHasExpectedHtml('<p>AM.</p>'))
		})
		it('1 1 1 1 A', () => {
			loadTestPage({ segment: 'hrs12' })
				.type('1')
				.should('have.value', '01:30 PM')
				.then(a11yHasExpectedHtml('<p>1.</p>'))
				.type('1')
				.should('have.value', '11:30 PM')
				.then(a11yHasExpectedHtml(a11yInitialHtml().minutes))
				.type('1')
				.should('have.value', '11:01 PM')
				.then(a11yHasExpectedHtml('<p>1.</p>'))
				.type('1')
				.should('have.value', '11:11 PM')
				.then(a11yHasExpectedHtml(a11yInitialHtml().mode))
				.type('a')
				.should('have.value', '11:11 AM')
				.then(a11yHasExpectedHtml('<p>AM.</p>'))
		})
	})
}